#Variáveis em Python
#Não há necessidade de declará-las no início do programa
#Basta criar no meio do algoritmo com a atribuição

Nome = "Renan"
print(Nome) #Imprime a string "Renan"
print(type(Nome)) #Imprime o tipo da variável, String
Idade = 16
print(Idade) #Imprime o número 16
print(type(Idade)) #Imprime o tipo da variável, que é int
Altura = 1.74
print(Altura) #Imprime o número 1.74
print(type(Altura)) #Imprime o tipo da variável, que é Float
teste = True
print(teste) #Imprime o valor True
print(type(teste)) #Imprime o tipo da variável, que é Bool